package linearlistonarray;

/**
 *
 * @author Leu <@leunardosevla at twitter.com>
 */
public class LinearList {
    private int startList;
    private int endList;
    private int lengthList;
    private int highList;
    private int lowList;
    
    public LinearList () {
        
    } 

    public int getStartList() {
        return startList;
    }

    public void setStartList(int startList) {
        this.startList = startList;
    }

    public int getEndList() {
        return endList;
    }

    public void setEndList(int endList) {
        this.endList = endList;
    }

    public int getLengthList() {
        return lengthList;
    }

    public void setLengthList(int lengthList) {
        this.lengthList = lengthList;
    }

    public int getHighList() {
        return highList;
    }

    public void setHighList(int highList) {
        this.highList = highList;
    }

    public int getLowList() {
        return lowList;
    }

    public void setLowList(int lowList) {
        this.lowList = lowList;
    }

    
}
